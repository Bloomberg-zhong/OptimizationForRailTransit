    def Waiting_Time(self,
                     R_1 = None,
                     R_2 = None,
                     BETA = None,
                     F_H = None
                     ):
        """
        乘客出行时间 Z1
        乘客候车时间CW、乘客乘车时间CV以及乘客在站台的换乘时间CT构成
        Z1 = CW+ CV + CT
        """
        # CW乘客候车时间
        CW = (np.array(self.Q)*3600) / 2*((R_1 * self.depart_fre)+(R_2 * self.depart_fre))
        CW_sum = np.sum(CW)
        print(str(CW_sum))

        # CV 乘客在车时间
        Tij = self.Tij.fillna(0)
        Tij= np.array(Tij)

        # 乘客乘车时间由途中纯运行时间CVR
        CVR = np.sum(np.array(self.Q) * Tij)

        # 交路1的每列列车在车站K的停站时间——1
        ST_H_K_ave_1 = pd.DataFrame((np.array(self.ak) * self.D) / (self.V * self.N_V * BETA * F_H) + self.ST_0)

        # ST_0列车在车站开关门时间
        ST_K = (BETA * (np.array(self.Train_Values_of_Float)[:, 2:3])) * ST_H_K_ave_1
        ST_K = np.sum(ST_K)

        # ST列车在车站停留时间
        ST = sum(ST_K)

        # 乘客候车时间CW, 乘客在车时间CV，乘客换乘时间CT
        CV = CVR + ST
        print(str(CV))

        # 乘客换乘时间
        Time = (3600 / (2 * self.depart_fre)) + self.T0
        CT_I_J = self.CT_I_J.replace('A', Time)

        # 乘客在站台的换乘时间CT
        CT = np.sum(np.array(CT_I_J) * np.array(self.Q))
        print(str(CT))









